def add(a, b):
    answer = a + b
    return answer
def substract(a, b):
    answer = a - b
    return answer
def multiply(a, b):
    answer = a * b
    return answer
def divide(a, b):
    answer = a / b
    return answer
def exp(a, b):
    answer = a % b
    return answer

